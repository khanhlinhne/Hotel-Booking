    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        int hotelId = Integer.parseInt(request.getParameter("hotelid"));
        request.setAttribute("hotelid", hotelId);
        request.getRequestDispatcher("/view/admin/addroom.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String name = request.getParameter("name");
        String image = request.getParameter("image");
        int numOfPeople = Integer.parseInt(request.getParameter("numOfPeople"));
        float area = Float.parseFloat(request.getParameter("area"));
        float price = Float.parseFloat(request.getParameter("price"));
        boolean isActive = Boolean.parseBoolean(request.getParameter("isActive"));
        String description = request.getParameter("description");
        int hotelid = Integer.parseInt(request.getParameter("hotelid"));
        int typeid = Integer.parseInt(request.getParameter("typeid"));
        new RoomDBContext().addRoom(new Room(0, name, image, numOfPeople, area, 1, price, isActive, description, hotelid, typeid));
        response.sendRedirect("roommanagement?hotelid="+hotelid);
    }

